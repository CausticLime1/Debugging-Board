/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
	SCB->VTOR = 0x08000000;
	HAL_Init();
	SystemClock_Config();
	MX_GPIO_Init();
	MX_SPI3_Init();

	/* Initialize Display */
	LCD_Init();

	/* Test: Turn Screen RED */
	LCD_Fill(0xF800);
	HAL_Delay(1000);

	/* Test: Turn Screen GREEN */
	LCD_Fill(0x07E0);

  /* BLINK FIRST - PROVE LIFE */
  /* If this runs, we know Clocks and Power are good */
	for(int i=0; i<10; i++) {
		HAL_GPIO_TogglePin(GPIOA, GPIO_PIN_10);
		HAL_Delay(50);
	}

	/* NOW try USB */
	MX_USB_DEVICE_Init();

	while (1)
	{
		HAL_GPIO_TogglePin(GPIOA, GPIO_PIN_10);
		HAL_Delay(500);
	}
}

